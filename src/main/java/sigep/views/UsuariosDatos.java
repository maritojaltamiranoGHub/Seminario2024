    private void cargarRoles() {
        RolesDAO rolesdao = new RolesDAO();
        List<Roles> roles = rolesdao.getRoles();

        jComboBox1.removeAllItems();

        for (Roles rol : roles) {
            jComboBox1.addItem(rol.toString());
        }
    }
    
