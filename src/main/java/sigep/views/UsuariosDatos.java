    private void jButtonActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonActualizarActionPerformed
        // Recuperar datos del formulario
        String userIdString = jTextField3.getText().trim();
        int userId;
        if (!userIdString.isEmpty()) {
             userId = Integer.parseInt(userIdString);
        } else {
        
             userId = 0; // O cualquier otro valor que indique un nuevo registro
        }
        
        String usuarioNombre = jTextField1.getText().trim();
        
        String usuarioRol = jComboBox1.getSelectedItem().toString();
        // Extraemos los dos primeros caracteres (el ID del rol)
        String idRolExtraido = usuarioRol.substring(0, 2);
        // Convertimos el idRolExtraido a un entero (si es necesario para la actualización)
        int idRolEntero = Integer.parseInt(idRolExtraido);
        
        String clave = jTextField2.getText().trim();

        // Validar datos
        if (usuarioNombre.isEmpty() || clave.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Los campos 'Nombre' y 'Clave' no pueden estar vacíos.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        // Crear el objeto Usuario
        Usuarios usuario = new Usuarios();
        usuario.setIdUsuario(userId);
        usuario.setNombre(usuarioNombre);
        usuario.setClave(clave);
        usuario.setIdRol(idRolEntero);
        
        // Actualizar en la base de datos
        UsuariosDAO usuariosDAO = new UsuariosDAOImpl();

        if (usuario.getIdUsuario() > 0) {
                usuariosDAO.actualizar(usuario);    
        } else {
                usuariosDAO.ingresar(usuario);
        } 

        this.dispose();
    }//GEN-LAST:event_jButtonActualizarActionPerformed

